    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        boolean hasil = false;
        hasil = jc.insert(txtKdLembur.getText(), Short.parseShort(txtLamaLembur.getText()),
                txtKdLembur.isEnabled());
        String pesan = "Gagal Menyimpan Data";
        if (hasil) {
            pesan = "Berhasil menyimpan Data";
        }
        jc.bindingALL(tblJenisLembur, header);
        JOptionPane.showMessageDialog(this, pesan);
        reset();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int i = JOptionPane.showConfirmDialog(this, "Apakah mau dihapus?");
        if (i == 0) {
            String pesan = "Gagal Hapus";
            boolean hasil = jc.
                    delete(txtKdLembur.getText());
            if (hasil) {
                pesan = "Berhasil hapus";
            }
            JOptionPane.showMessageDialog(this, pesan);
            jc.bindingALL(tblJenisLembur, header);
            reset();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblJenisLemburMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJenisLemburMouseClicked
        txtKdLembur.setText(tblJenisLembur.getValueAt(tblJenisLembur.getSelectedRow(), 1) + "");
        txtLamaLembur.setText(tblJenisLembur.getValueAt(tblJenisLembur.getSelectedRow(), 2) + "");
        txtKdLembur.setEnabled(false);
        txtLamaLembur.setEnabled(true);
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_tblJenisLemburMouseClicked

